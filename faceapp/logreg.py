def evaluate_predictions(y_true, y_pred, threshold=None):
    """
    Calculates and prints evaluation metrics for given predictions.
    y_true: The real correct answers.
    y_pred: The answers given by our machine based on a specific cutoff.
    threshold (optional): The cutoff number used to get y_pred, just for showing.
    """
    if threshold is not None:
        print(f"\n--- Checking Results with Cutoff {threshold:.3f} ---")
    else:
        print("\n--- Checking Results with Default Cutoff (0.5) ---") # This is usually 0.5 for predict()


    # Calculate and print the accuracy (how many correct answers overall)
    accuracy = accuracy_score(y_true, y_pred)
    print(f"  Accuracy: {accuracy:.4f}")

    # Print the confusion matrix (the table of mistakes)
    print("  Confusion Matrix ( Mistake Table ):")
    # Remember, in scikit-learn, the table layout is typically:
    # [[Correctly said NO , Wrongly said YES  (False Positives)]
    #  [Wrongly said NO   , Correctly said YES (True Positives)]]
    print(confusion_matrix(y_true, y_pred))

    # Print a detailed report of mistakes
    print("  Detailed Mistake Report:")
    # zero_division=0 means show 0 instead of an error if a calculation involves dividing by zero
    print(classification_report(y_true, y_pred, zero_division=0))

